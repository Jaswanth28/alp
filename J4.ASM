ASSUME CS:CODE, DS:DATA
org 100h
DATA SEGMENT
	LIST Db 1H,8H,3H
	count equ 03H
	MAX DB 01H DUP(?)
DATA ENDS
CODE SEGMENT
	START:
		MOV AX,DATA
		MOV DS,AX
		MOV SI,OFFSET LIST
		MOV CL,COUNT
		MOV AL,[SI]
	AGAIN:
		CMP AL,[SI+1]
		JNL NEXT
		MOV AL,[SI+1]
	NEXT:
		INC SI
		DEC CL
		CMP CL,0H
		JG AGAIN
		MOV BL,AL
		MOV AH,4CH
	MOV AL,0H
	INT 03H
CODE ENDS
END START
INPUT/OUTPUT: